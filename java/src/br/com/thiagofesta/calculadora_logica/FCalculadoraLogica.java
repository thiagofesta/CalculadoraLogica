    /**
     * Instância a classe CalculadoraLogica e resolve a expressao
     */
    private void resolve()
    {
            CalculadoraLogica calc = new CalculadoraLogica();

            if(jTextField.getText().compareTo("") > 0)
            {
                    if(calc.isValid(jTextField.getText()))
                    {
                            calc.tokenize(jTextField.getText());
                            jTextField.setText(calc.start());
                    }
                    else
                    {
                            jTextField.setText("EXPRESSÃO INVÁLIDA!");
                    }
                    
                    this.resultado = true;
            }
    }
    
    /**
     * Adiciona dado a expressão
     * @param texto Texto a ser adicionado no jTextField
     */
    private void setExpressao(String texto)
    {
            if(this.resultado)
            {
                   jTextField.setText("");
            }

            jTextField.setText(jTextField.getText() + texto + " ");
            this.resultado = false;
    }

    /**
     * Exibe cursor como seta normal, ou como mão
     * @param hover Se verdadeiro mouse está em cima do botão
     */
    private void setCursor(boolean hover)
    {
            Cursor cursor;

            if(hover)
            {
                   cursor = Cursor.getPredefinedCursor(Cursor.HAND_CURSOR);
            }
            else
            {
                    cursor = Cursor.getDefaultCursor();
            }

            setCursor(cursor);
    }
    
