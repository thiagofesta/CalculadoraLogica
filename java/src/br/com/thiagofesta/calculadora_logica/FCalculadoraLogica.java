    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
            if(evt.getKeyCode() == evt.VK_T)
            {
                    setExpressao("T");
            }
            else if(evt.getKeyCode() == evt.VK_F)
            {
                    setExpressao("F");
            }
            else if(evt.getKeyCode() == evt.VK_N)
            {
                    setExpressao("~");
            }
            else if(evt.getKeyCode() == evt.VK_E)
            {
                    setExpressao("^");
            }
            else if(evt.getKeyCode() == evt.VK_O)
            {
                    setExpressao("v");
            }
            else if(evt.getKeyCode() == evt.VK_X)
            {
                    setExpressao("x");
            }
            else if(evt.getKeyCode() == evt.VK_C)
            {
                    setExpressao("->");
            }
            else if(evt.getKeyCode() == evt.VK_B)
            {
                    setExpressao("<->");
            }
            else if(evt.getKeyCode() == evt.VK_A)
            {
                    setExpressao("(");
            }
            else if(evt.getKeyCode() == evt.VK_S)
            {
                    setExpressao(")");
            }
            else if(evt.getKeyCode() == evt.VK_EQUALS)
            {
                    resolve();
            }
            else if(evt.getKeyCode() == evt.VK_DELETE)
            {
                    jTextField.setText("");
            }
            else if(evt.getKeyCode() == evt.VK_BACK_SPACE)
            {
                    if(jTextField.getText().length() >= 2)
                    {
                            jTextField.setText( jTextField.getText().substring(0, (jTextField.getText().length()-2) ) );
                    }
            }
    }//GEN-LAST:event_formKeyPressed
    
